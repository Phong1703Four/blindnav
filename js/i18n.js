  init() {
    const saved = localStorage.getItem(this.STORAGE_KEY);
    if (saved && this.translations[saved]) {
      this.currentLang = saved;
    }
    this.applyLanguage();
  },

  // Get translation with variable interpolation
  t(key, vars = {}) {
    const dict = this.translations[this.currentLang] || this.translations.vi;
    let text = dict[key];
    if (text === undefined) {
      // Fallback to Vietnamese
      text = this.translations.vi[key];
    }
    if (text === undefined) {
      console.warn(`Missing i18n key: ${key}`);
      return key;
    }
    // Replace {var} placeholders
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    });
    return text;
  },

  setLanguage(lang) {
    if (!this.translations[lang]) return;
    this.currentLang = lang;
    localStorage.setItem(this.STORAGE_KEY, lang);
    this.applyLanguage();
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    // Update theme toggle tooltip
    if (typeof ThemeManager !== 'undefined') {
      ThemeManager._updateToggleIcon();
    }
  },

  applyLanguage() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const vars = el.getAttribute('data-i18n-vars');
      let parsedVars = {};
      if (vars) {
        try { parsedVars = JSON.parse(vars); } catch(e) {}
      }
      const text = this.t(key, parsedVars);
      
      // Check if it's an input placeholder
      if (el.hasAttribute('data-i18n-placeholder')) {
        el.placeholder = text;
      } else if (el.hasAttribute('data-i18n-title')) {
        el.title = text;
      } else {
        el.textContent = text;
      }
    });

    // Update page title
    document.title = this.t('app.title');
  },

  // Get current language display name
  getCurrentName() {
    return this.currentLang === 'vi' ? '🇻🇳 Tiếng Việt' : '🇬🇧 English';
  },

  // Get language code for speech synthesis
  getSpeechLang() {
    return this.currentLang === 'vi' ? 'vi-VN' : 'en-US';
  }
